func TestSourceYAMLRoundtrip(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "source.yaml")

	original := &fileformat.SourceYAML{
		Name:        "telegram",
		Description: "Telegram Bot API",
		Version:     "7.2",
		DocOrigin: &fileformat.DocOrigin{
			URL:         "https://core.telegram.org/bots/api",
			ContentType: "scrape",
			Reason:      "no LLM-friendly format found",
			Validated:   true,
		},
		Triggers: []string{"telegram", "bot api", "sendMessage"},
		Stats:    fileformat.SourceStats{Sections: 3, Methods: 50, Concepts: 5, Gotchas: 8},
	}

	if err := fileformat.WriteSourceYAML(path, original); err != nil {
		t.Fatalf("write: %v", err)
	}

	loaded, err := fileformat.ReadSourceYAML(path)
	if err != nil {
		t.Fatalf("read: %v", err)
	}

	if loaded.Name != original.Name {
		t.Errorf("name: got %q, want %q", loaded.Name, original.Name)
	}
	if loaded.DocOrigin.URL != original.DocOrigin.URL {
		t.Errorf("doc_origin url: got %q", loaded.DocOrigin.URL)
	}
	if loaded.Stats.Methods != 50 {
		t.Errorf("stats.methods: got %d, want 50", loaded.Stats.Methods)
	}
}
